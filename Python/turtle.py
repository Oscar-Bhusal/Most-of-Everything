# draw square in Python Turtle
import turtle

s = int(input("Enter the length of the side of the Square: "))

# drawing first side
turtle.forward(s) # Forward turtle by s units
turtle.left(90) # Turn turtle by 90 degree

# drawing second side
turtle.forward(s) # Forward turtle by s units
turtle.left(90) # Turn turtle by 90 degree

# drawing third side
turtle.forward(s) # Forward turtle by s units
turtle.left(90) # Turn turtle by 90 degree

